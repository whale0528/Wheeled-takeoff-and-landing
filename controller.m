%% =========================================================================
%% 纵向短周期扰动运动方程动力系数计算 (a24, a25, a34)
%% =========================================================================
% 假设此处取你的抬轮速度进行单点测试
V_test = 136;   % VR = 40.0 m/s
zeta = 0.8;
wd = 15;
Tr = 1.2;
% 速度保护阈值（你前面定义的 V_min = 1），防止 V=0 时除零报错
if V_test < V_min
    a24 = 0;
    a25 = 0;
    a34 = 0;
else
    % 1. 计算当前飞行状态下的动压 q (Pa)
    q_dynamic = 0.5 * rho0 * V_test^2;

    % 2. 计算 a24：静稳定动力系数 (s^-2)
    % 公式: a24 = - Mz_alpha / Izz = - (Cma * q * S * c) / Izz
    a24 = -(Cma * q_dynamic * S_ref * c_ref) / Izz;

    % 3. 计算 a25：舵面动力系数 (s^-2)
    % 公式: a25 = - Mz_delta / Izz = - (Cmde * q * S * c) / Izz
    % 备注：根据文档中的方程 \dot{\omega}_z = ... - a25 * delta_z
    a25 = -(Cmde * q_dynamic * S_ref * c_ref) / Izz;

    % 4. 计算 a34：法向力动力系数 (s^-1)
    % 公式: a34 = Y_alpha / (m * V) = (CLa * q * S) / (m * V)
    a34 = (CLa * q_dynamic * S_ref+D0) / (m * V_test);
end
Kn_pitch=(2.2*wd^2*g)/(Tr*(wd^2-a24)*V_test*a34)
K_alpha=(wd^2-a24)/(2*zeta*wd)
Kw_pitch=-(2*zeta*wd) / a25
%% =========================================================================
%% 打印计算结果
%% =========================================================================
fprintf('\n=== 当前计算速度 V = %.1f m/s ===\n', V_test);
fprintf('动压 q_dynamic  = %.2f Pa\n', q_dynamic);
fprintf('静稳定动力系数 a24 = %8.4f s^-2 \n', a24);
fprintf('舵面动力系数   a25 = %8.4f s^-2 \n', a25);
fprintf('法向力动力系数 a34 = %8.4f s^-1 \n', a34);
